// Create a new textbox element
const textBox = document.createElement('input');

// Set the textbox's type and value properties
textBox.type = 'text';
textBox.value = 'Hello World';

// Set the textbox's style properties to make it red
textBox.style.color = 'red';

// Append the textbox to the body of the HTML document
document.body.appendChild(textBox);
